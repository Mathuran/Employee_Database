    private void AddButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddButtonMousePressed
        // TODO add your handling code here:

            int eNum = Integer.parseInt(eNumTxt.getText());
            String workLocaTxt = WorkLoca.getSelectedItem().toString();
            double deduc = Double.parseDouble(DeductionText.getText());
            String sex = Sex.getSelectedItem().toString();
            
            if (PTEbutton.isSelected() == true){
                System.out.println("PTE");

                int HperW = Integer.parseInt(HperWText.getText());
                int WperY = Integer.parseInt(WperYText.getText());
                double wage = Double.parseDouble(HWageText.getText());
            
                PartTimeEmployee PTE = new PartTimeEmployee(eNum,FName.getText(), LName.getText(),workLocaTxt,deduc,sex,HperW, WperY, wage);
                Hash.addEmployee(PTE); 
                
                DefaultTableModel model = (DefaultTableModel) PTEtable.getModel();
                model.addRow(new Object[]{FName.getText(), LName.getText(),sex,workLocaTxt,deduc,HperW, WperY,wage, PTE.calcNetAnnualIncome()});
                
            }else if (FTEbutton.isSelected() == true){
               System.out.println("FTE");

                double aSal = Double.parseDouble(ASalText.getText());
                
                FullTimeEmployee FTE = new FullTimeEmployee(eNum,FName.getText(), LName.getText(),workLocaTxt,deduc,sex,aSal);
                Hash.addEmployee(FTE);
                
                DefaultTableModel model = (DefaultTableModel) FTEtable.getModel();
                model.addRow(new Object[]{FName.getText(), LName.getText(),sex,workLocaTxt,deduc,aSal,FTE.calcNetAnnualIncome()});
            } else {
            }
            
            System.out.println("button pressed");
    }//GEN-LAST:event_AddButtonMousePressed

